                  <p>How MANGA is made?<br>
                    In Japan, manga starts out serialized in chapter format, along with other manga series, in monthly or weekly anthologies such as Weekly Shonen Jump. Unlike American comics, which are released individually on a series-by-series basis, manga is grouped by interest or type in these anthologies. And these anthologies can get gigantic!
                    Once a manga has been serialized for a while, the chapters are collected into graphic novels. Sometimes, if a series is very successful, the graphic novels will be republished in special omnibus editions, which can include two or three volumes in one beautifully printed edition (One Piece and Ranma ½ are great examples), some including specially drawn full-color artwork, like in our Monster: Perfect and Death Note Black editions.
                    In addition, spin-off art books and special releases, including novels, are often published for some of our more popular series, such as Naruto and Tokyo Ghoul.</p>
